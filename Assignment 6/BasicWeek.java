package lab_6;

public class BasicWeek {
    // Initializing variables
    public String day_1 = "Monday";
    public String day_2 = "Tuesday";
    public String day_3 = "Wednesday";
    public String day_4 = "Thursday";
    public String day_5 = "Friday";
    public String day_6 = "Saturday";
    public String day_7 = "Sunday";

    // printing out the days
    public String printDays() {
        String s =  "The basic days of the week are: "  + day_1 + " "  + day_2 + " "  + day_3 + " "  + day_4 + " " 
            + day_5 + " "  + day_6 + " "  + day_7 ;   
        return s;
    }
}